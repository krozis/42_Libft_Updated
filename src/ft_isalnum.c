#include "libft.h"

t_bool	ft_isalnum(int c)
{
	if (c >= '0' && c <= '9')
		return (TRUE);
	else if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (TRUE);
	return (FALSE);
}
